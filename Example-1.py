# Запитуємо користувача про довжину та ширину прямокутника
довжина = float(input("Введіть довжину прямокутника: "))
ширина = float(input("Введіть ширину прямокутника: "))

# Обчисліть площу прямокутника
площа = довжина * ширина

# Обчисліть периметр прямокутника
периметр = 2 * (довжина + ширина)

# Виведіть результати
print("Площа прямокутника: ", площа)
print("Периметр прямокутника: ", периметр)
